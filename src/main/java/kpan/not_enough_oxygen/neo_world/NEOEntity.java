package kpan.not_enough_oxygen.neo_world;

public class NEOEntity {

    public float x;
    public float y;
    public float z;
    public float temperature;

    public NEOEntity(float x, float y, float z, float temperature) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.temperature = temperature;
    }

}
